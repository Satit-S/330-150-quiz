// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏™‡∏∏‡πà‡∏°‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°
function shuffleQuestions(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏™‡∏î‡∏á‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°
function showQuestion() {
    if (currentQuestionIndex >= totalQuestions) {
        showResults();
        return;
    }
    
    const currentQuestion = questions[currentQuestionIndex];
    questionText.textContent = currentQuestion.question;
    choicesContainer.innerHTML = '';
    
    // ‡∏™‡∏∏‡πà‡∏°‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
    const shuffledChoices = [...currentQuestion.choices].sort(() => Math.random() - 0.5);
    
    shuffledChoices.forEach(choice => {
        const button = document.createElement('button');
        button.textContent = choice;
        button.classList.add('choice-btn');
        button.onclick = () => checkAnswer(choice, currentQuestion.answer, currentQuestion.category);
        choicesContainer.appendChild(button);
    });
    
    feedbackElement.textContent = '';
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö
function checkAnswer(selectedChoice, correctAnswer, category) {
    const isCorrect = (selectedChoice === correctAnswer);
    
    if (!scoreByCategory[category]) {
        scoreByCategory[category] = { correct: 0, total: 0 };
    }
    
    scoreByCategory[category].total++;
    
    if (isCorrect) {
        feedbackElement.textContent = '‚úÖ ‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á!';
        feedbackElement.classList.remove('incorrect');
        feedbackElement.classList.add('correct');
        totalScore++;
        scoreByCategory[category].correct++;
    } else {
        feedbackElement.textContent = `‚ùå ‡∏ú‡∏¥‡∏î! ‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö‡∏ó‡∏µ‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡∏Ñ‡∏∑‡∏≠: ${correctAnswer}`;
        feedbackElement.classList.remove('correct');
        feedbackElement.classList.add('incorrect');
    }
    
    // ‡∏õ‡∏¥‡∏î‡∏õ‡∏∏‡πà‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å‡∏ï‡∏≠‡∏ö‡πÅ‡∏•‡πâ‡∏ß
    const buttons = choicesContainer.querySelectorAll('.choice-btn');
    buttons.forEach(btn => btn.disabled = true);
    
    // ‡∏£‡∏≠‡∏™‡∏±‡∏Å‡∏Ñ‡∏£‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß‡πÑ‡∏õ‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°‡∏ñ‡∏±‡∏î‡πÑ‡∏õ
    setTimeout(() => {
        currentQuestionIndex++;
        showQuestion();
    }, 2000);
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
function showResults() {
    quizArea.style.display = 'none';
    resultArea.style.display = 'block';

    totalScoreElement.textContent = `‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î: ${totalScore} ‡∏à‡∏≤‡∏Å ${totalQuestions}`;
    categoryScoresList.innerHTML = '';

    for (const category in scoreByCategory) {
        const categoryData = scoreByCategory[category];
        const percentage = (categoryData.correct / categoryData.total) * 100;
        const li = document.createElement('li');
        li.textContent = `${category}: ${categoryData.correct} ‡∏à‡∏≤‡∏Å ${categoryData.total} ‡∏Ç‡πâ‡∏≠ (${percentage.toFixed(0)}%)`;

        if (percentage < 60) {
            li.classList.add('red-text');
            const recommendation = document.createElement('span');
            recommendation.textContent = ' üö® ‡∏Ñ‡∏ß‡∏£‡∏ó‡∏ö‡∏ó‡∏ß‡∏ô‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÄ‡∏ï‡∏¥‡∏°';
            li.appendChild(recommendation);
        }

        categoryScoresList.appendChild(li);
    }
}

